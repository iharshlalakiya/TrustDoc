def analyze_metadata(file_path: str, file_type: str) -> dict:
    """
    Analyse document metadata and return a structured forensic report.

    Args:
        file_path: Absolute or relative path to the document on disk.
        file_type: File extension without dot (pptx, pdf).

    Returns:
        {
            creator, last_modified_by, created_timestamp, modified_timestamp,
            revision_count, app_version, generator_tool,
            total_editing_time_minutes,
            flags: [ { flag_id, description, severity } ],
            metadata_integrity: "Low" | "Medium" | "High",
            confidence: float,
        }

    Raises:
        FileNotFoundError: If the document does not exist on disk.
        ValueError: If the file_type is unsupported.
    """
    path = Path(file_path)
    if not path.is_file():
        raise FileNotFoundError(f"Document not found: {file_path}")

    normalized_type = file_type.lower().lstrip(".")

    if normalized_type == "pptx":
        meta = _extract_pptx_metadata(path)
    elif normalized_type == "pdf":
        meta = _extract_pdf_metadata(path)
    else:
        raise ValueError(f"Unsupported file type for metadata analysis: {file_type}")

    flags = _detect_flags(meta, normalized_type)
    integrity, confidence = _compute_integrity_and_confidence(flags)

    # Determine generator_tool: the first programmatic signature found in any field
    generator_tool: str | None = None
    for field in ("application", "subject", "company", "creator"):
        hit, sig = _contains_generator_signature(meta.get(field))
        if hit:
            generator_tool = sig
            break
    # PDF: also check producer
    if generator_tool is None:
        hit, sig = _contains_generator_signature(meta.get("producer"))
        if hit:
            generator_tool = sig

    return {
        "creator": meta.get("creator"),
        "last_modified_by": meta.get("last_modified_by"),
        "created_timestamp": _fmt_dt(meta.get("created_dt")),
        "modified_timestamp": _fmt_dt(meta.get("modified_dt")),
        "revision_count": meta.get("revision"),
        "app_version": meta.get("app_version"),
        "generator_tool": generator_tool,
        "total_editing_time_minutes": meta.get("total_time_minutes"),
        "flags": flags,
        "metadata_integrity": integrity,
        "confidence": confidence,
    }
