# PPTX helpers
# ---------------------------------------------------------------------------

def _parse_iso_datetime(value: str | None) -> datetime | None:
    """Parse an ISO-8601 datetime string to an aware datetime object."""
    if not value:
        return None
    value = value.strip().rstrip("Z")
    try:
        dt = datetime.fromisoformat(value)
        if dt.tzinfo is None:
            dt = dt.replace(tzinfo=timezone.utc)
        return dt
    except ValueError:
        return None


def _fmt_dt(dt: datetime | None) -> str | None:
    """Format a datetime to ISO-8601 string or return None."""
    if dt is None:
        return None
    return dt.strftime("%Y-%m-%dT%H:%M:%SZ")


def _xml_text(element: ET.Element | None, tag: str, ns: str = "") -> str | None:
    """Safely extract text from an XML element by tag."""
    if element is None:
        return None
    if ns:
        child = element.find(f"{{{ns}}}{tag}")
    else:
        child = element.find(tag)
    return child.text.strip() if child is not None and child.text else None


def _read_pptx_xml(file_path: Path) -> tuple[ET.Element | None, ET.Element | None]:
    """
    Open the PPTX (ZIP) and parse docProps/core.xml and docProps/app.xml.
    Returns (core_root, app_root).  Either may be None if the part is absent.
    """
    core_root: ET.Element | None = None
    app_root: ET.Element | None = None

    try:
        with zipfile.ZipFile(str(file_path), "r") as zf:
            names = zf.namelist()
            if "docProps/core.xml" in names:
                with zf.open("docProps/core.xml") as f:
                    core_root = ET.fromstring(f.read())
            if "docProps/app.xml" in names:
                with zf.open("docProps/app.xml") as f:
                    app_root = ET.fromstring(f.read())
    except (zipfile.BadZipFile, ET.ParseError):
        pass

    return core_root, app_root


def _extract_pptx_metadata(file_path: Path) -> dict:
    """
    Combine python-pptx core properties with raw XML parsing of
    docProps/core.xml and docProps/app.xml for full metadata coverage.
    """
    prs = Presentation(str(file_path))
    props = prs.core_properties
    core_root, app_root = _read_pptx_xml(file_path)

    # --- Core properties via python-pptx (convenient typed access) ---
    creator: str | None = props.author or None
    last_modified_by: str | None = props.last_modified_by or None
    created_dt: datetime | None = props.created
    modified_dt: datetime | None = props.modified
    revision: int | None = props.revision
    title: str | None = props.title or None
    subject: str | None = props.subject or None
    keywords: str | None = props.keywords or None

    # Normalise to aware datetimes
    if created_dt and created_dt.tzinfo is None:
        created_dt = created_dt.replace(tzinfo=timezone.utc)
    if modified_dt and modified_dt.tzinfo is None:
        modified_dt = modified_dt.replace(tzinfo=timezone.utc)

    # --- App properties via raw XML (python-pptx doesn't expose all of these) ---
    application: str | None = None
    app_version: str | None = None
    company: str | None = None
    total_time: int | None = None

    if app_root is not None:
        application = _xml_text(app_root, "Application", _APP_NS)
        app_version = _xml_text(app_root, "AppVersion", _APP_NS)
        company = _xml_text(app_root, "Company", _APP_NS)
        raw_total_time = _xml_text(app_root, "TotalTime", _APP_NS)
        if raw_total_time is not None:
            try:
                total_time = int(raw_total_time)
            except ValueError:
                total_time = None

    return {
        "creator": creator,
        "last_modified_by": last_modified_by,
        "created_dt": created_dt,
        "modified_dt": modified_dt,
        "revision": revision,
        "title": title,
        "subject": subject,
        "keywords": keywords,
        "application": application,
        "app_version": app_version,
        "company": company,
        "total_time_minutes": total_time,
    }


