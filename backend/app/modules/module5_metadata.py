# Constants
# ---------------------------------------------------------------------------

# Known programmatic / generator tool strings that indicate machine-generated docs
PROGRAMMATIC_TOOL_SIGNATURES: list[str] = [
    "pptxgenjs",
    "python-pptx",
    "libreoffice",
    "openoffice",
    "aspose",
    "spire",
    "googleslides",
    "google slides",
    "wps office",
    "keynote",
    "canva",
    "slidesgo",
    "templateswise",
    "slidescarnival",
    "chatgpt",
    "gemini",
    "copilot",
    "auto-generated",
    "autogenerated",
    "script",
    "bot",
    "tool",
]

# Known legitimate authoring applications (used in Flag 4 cross-check)
LEGITIMATE_APPLICATIONS: list[str] = [
    "microsoft office",
    "microsoft powerpoint",
    "microsoft word",
    "microsoft excel",
    "powerpoint",
    "word",
    "excel",
]

# XML namespace map used in OOXML core/app properties
_NS = {
    "cp": "http://schemas.openxmlformats.org/package/2006/metadata/core-properties",
    "dc": "http://purl.org/dc/elements/1.1/",
    "dcterms": "http://purl.org/dc/terms/",
    "xsi": "http://www.w3.org/2001/XMLSchema-instance",
    "vt": "http://schemas.openxmlformats.org/officeDocument/2006/docPropsVTypes",
    "": "http://schemas.openxmlformats.org/officeDocument/2006/extended-properties",
}

_APP_NS = "http://schemas.openxmlformats.org/officeDocument/2006/extended-properties"


