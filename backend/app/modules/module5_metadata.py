"""
Module 5 – Metadata Analysis
Extracts and forensically evaluates document metadata for PPTX and PDF files.
Flags suspicious authorship signals, timestamp anomalies, and generator tool
inconsistencies that indicate programmatic or fraudulent document creation.
"""

import zipfile
import xml.etree.ElementTree as ET
from datetime import datetime, timezone
from pathlib import Path

from pptx import Presentation
import fitz  # PyMuPDF

